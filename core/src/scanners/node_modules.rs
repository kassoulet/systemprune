//! Scanner for `node_modules` directories.
//!
//! Detects `node_modules` folders by walking the home directory and
//! identifies the owning project root via the nearest parent `package.json`.

use super::fs_scan::{delete_dir, find_dirs_named, home, HOME_EXCLUDE};
use super::Scanner;
use crate::errors::EngineError;
use crate::models::{Category, Engine, PrunableItem, Status};
use async_trait::async_trait;
use std::collections::BTreeMap;
use std::path::{Path, PathBuf};

const SOURCE: &str = "node_modules";

pub struct NodeModulesScanner;

impl Default for NodeModulesScanner {
    fn default() -> Self {
        Self::new()
    }
}

impl NodeModulesScanner {
    pub const fn new() -> Self {
        Self
    }

    /// Given a `node_modules` path, walk up to find the nearest parent
    /// containing a `package.json`. Returns that parent, or the
    /// `node_modules` parent if none is found.
    fn project_root(nm_path: &Path) -> PathBuf {
        let mut cursor = nm_path.parent().unwrap_or(nm_path);
        loop {
            if cursor.join("package.json").is_file() {
                return cursor.to_path_buf();
            }
            match cursor.parent() {
                Some(p) => cursor = p,
                None => return nm_path.parent().unwrap_or(nm_path).to_path_buf(),
            }
        }
    }
}

#[async_trait]
impl Scanner for NodeModulesScanner {
    fn source(&self) -> &'static str {
        SOURCE
    }
    fn engine(&self) -> Engine {
        Engine::NodeModules
    }
    fn binary(&self) -> &'static str {
        "node"
    }

    fn is_available(&self) -> bool {
        true
    }

    async fn get_items(&self) -> Result<Vec<PrunableItem>, EngineError> {
        let root = home();
        let found = find_dirs_named(&root, "node_modules", HOME_EXCLUDE);
        let items = found
            .into_iter()
            .map(|(path, size)| {
                let project = Self::project_root(&path);
                let project_name = project
                    .file_name()
                    .map(|n| n.to_string_lossy().into_owned())
                    .unwrap_or_else(|| path.display().to_string());
                let mut extra = BTreeMap::new();
                extra.insert("path".into(), path.display().to_string());
                extra.insert("project_root".into(), project.display().to_string());
                PrunableItem {
                    id: path.display().to_string(),
                    name: project_name,
                    engine: Engine::NodeModules,
                    source: SOURCE.to_string(),
                    category: Category::NodeModules,
                    size_bytes: size,
                    status: Status::Unused,
                    extra,
                }
            })
            .collect();
        Ok(items)
    }

    async fn delete_item(&self, item: &PrunableItem) -> Result<(), EngineError> {
        delete_dir(&item.id).await
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;

    #[test]
    fn project_root_finds_package_json() {
        let tmp = tempfile::tempdir().unwrap();
        let project = tmp.path().join("my-app");
        fs::create_dir_all(project.join("src")).unwrap();
        fs::write(project.join("package.json"), "{}").unwrap();
        let nm = project.join("node_modules");
        fs::create_dir_all(&nm).unwrap();

        assert_eq!(NodeModulesScanner::project_root(&nm), project);
    }

    #[test]
    fn project_root_falls_back() {
        let tmp = tempfile::tempdir().unwrap();
        let nm = tmp.path().join("node_modules");
        fs::create_dir_all(&nm).unwrap();

        assert_eq!(
            NodeModulesScanner::project_root(&nm),
            tmp.path().to_path_buf()
        );
    }
}
